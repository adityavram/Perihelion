def miller_rabin_analogy():
    """
    How Miller-Rabin circumvents circular dependencies.
    """
    print("\n" + "="*70)
    print("THE MILLER-RABIN ANALOGY")
    print("="*70)
    print("""
Miller-Rabin primality test:
  - Write N-1 = 2^s · d
  - Pick random a ∈ [2, N-2]
  - Compute a^d mod N
  - Square repeatedly: a^(d·2^i) mod N
  - Look for witnesses of compositeness

Key insight:
  - We DON'T compute a^((N-1)/2) mod p
  - We compute a^((N-1)/2) mod N directly
  - If N = pq, the result differs from mod p or mod q
  - The difference reveals factors!

The analogy for square roots:
  - We DON'T compute √d mod p and √d mod q separately
  - We try to compute √d mod N directly
  - If N = pq, the computation behaves differently
  - Can we detect this difference?

The Miller-Rabin "witness":
  - a^(N-1) ≡ 1 (mod N) by Fermat for prime N
  - But for composite N = pq:
    - a^((N-1)/2) might not be ±1 mod N
    - The "gap" reveals factors

The square root "witness":
  - For prime p, √d mod p has 1 or 2 solutions
  - For composite N = pq, √d mod N has up to 4 solutions
  - The "multiplicity" indicates compositeness
  - But computing √d mod N is hard

Alternative:
  - Don't compute √d mod N
  - Compute something RELATED to √d
  - Use the ring Z[√d]/(N)
  - Look for "anomalies" that reveal factors

In Z[√d]/(N):
  - Elements are a + b√d with operations mod N
  - We can compute (a + b√d)² mod N
  - If (a + b√d)² ≡ d (mod N), then we've found √d
  - The computation (a + b√d)² = a² + 2ab√d + b²d
  - Mod N: a² + b²d ≡ something (mod N), 2ab ≡ 0 (mod N)?
  - Wait, this requires 2ab ≡ 0 (mod N) for the √d coefficient
  
This gives us:
  - Either b = 0: Then a² ≡ d (mod N), back to square roots
  - Or a = 0: Then b²d ≡ d (mod N), so b² ≡ 1 (mod N)
  - Or N | 2ab: Since N is large, this means a or b is multiple of N

So we're back to computing square roots mod N.

The circular dependency remains:
  - To compute in Z[√d]/(N), we need arithmetic mod N
  - Arithmetic mod N works the same for prime or composite N
  - We don't "see" the factors in the operations
  
UNLESS:
  - The operations fail or behave differently
  - Like in Miller-Rabin where a^((N-1)/2) ≠ ±1 mod N
  - Is there an analogous "square root test"?
""")


