class Ideal:
    """
    Ideal in Z[√d].
    
    An ideal is generated by two elements: (a, b) where:
      - a ∈ Z
      - b ∈ Z[√d]
    
    For principal ideals: (α) for some α ∈ Z[√d].
    
    For prime ideals above p (when p splits):
      - P = (p, √d - r) where r² ≡ d (mod p)
      - P' = (p, √d + r)
    
    These multiply: P × P' = (p)
    """
    
    def __init__(self, a: int, b_coeffs: Tuple[int, int], d: int):
        """
        Ideal generated by:
          - a ∈ Z
          - b = b_coeffs[0] + b_coeffs[1]√d ∈ Z[√d]
        """
        self.a = a
        self.b_coeffs = b_coeffs  # (c0, c1) where b = c0 + c1√d
        self.d = d
    
    def norm(self) -> int:
        """
        Norm of an ideal.
        
        For principal ideal (α): N((α)) = |N(α)|
        
        For prime ideals above p:
          - If p splits: N(P) = N(P') = p
          - If p ramifies: N(P) = p
          - If p is inert: N((p)) = p²
        """
        # This is simplified - actual norm computation requires class group
        return abs(self.a)
    
    def __repr__(self):
        return f"Ideal({self.a}, {self.b_coeffs}, d={self.d})"


def ideal_multiplication(P: Ideal, Q: Ideal) -> Ideal:
    """
    Multiply two ideals in Z[√d].
    
    P × Q = generated by products of generators.
    
    For principal ideals:
      (α) × (β) = (αβ)
    
    For prime ideals above p and q:
      P_p × P_q = ideal with generators p, q, and cross terms
    """
    # Simplified: principal ideal case
    # In general, ideal multiplication is more complex
    return Ideal(P.a * Q.a, P.b_coeffs, P.d)


