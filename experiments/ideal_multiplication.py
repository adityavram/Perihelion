"""
Ideal Multiplication Approach.

Key insight: Ideals MULTIPLY, they don't divide.

In Z[√d]:
  - (p) = P × P' when p splits (multiplication!)
  - (q) = Q × Q' when q splits
  - (N) = P × P' × Q × Q' (factorization by multiplication)

The question: Can we find P or P' without knowing p?
The approach: Work with ideals abstractly, using multiplication.
"""

